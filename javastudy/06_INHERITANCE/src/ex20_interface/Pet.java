package ex20_interface;

public class Pet {

	// field
	private String name;

	// constructor
	public Pet(String name) {
		super();
		this.name = name;
	}

	// method
	
	// getter setter
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
	
}
